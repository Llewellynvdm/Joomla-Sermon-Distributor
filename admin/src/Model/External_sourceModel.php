	public function clearLocalListing($id) 
	{
		// clear local listings
		if ($id > 0)
		{
			$db = Factory::getDbo();
			$query = $db->getQuery(true);
 
			$conditions = array(
				$db->quoteName('external_source') . ' = ' . (int) $id
			);
 
